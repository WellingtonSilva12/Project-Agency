const ul = document.querySelector('nav ul')
const fixed = document.querySelector('.main')
const menuBtn = document.querySelector('.menu-btn i')

function menuShow() {
  if (ul.classList.contains('')) {
    ul.classList.toggle('open')
    fixed.classList.toggle('open')
  } else {
    ul.classList.toggle('open')
    fixed.classList.toggle('open')
  }
}
